__device__ __forceinline__ uint32_t hamsi_rotr32(uint32_t x, int n) {
    return (x >> n) | (x << (32 - n));
}

// Hamsi SubBytes transformation
__device__ void hamsi_sub_bytes(uint32_t state[16]) {
    for (int i = 0; i < 16; i++) {
        uint8_t* bytes = (uint8_t*)&state[i];
        for (int j = 0; j < 4; j++) {
            bytes[j] = hamsi_sbox[bytes[j]];
        }
    }
}

// Hamsi ShiftRows transformation (4x4 matrix)
__device__ void hamsi_shift_rows(uint32_t state[16]) {
    uint32_t temp[16];
    for (int i = 0; i < 16; i++) {
        temp[i] = state[i];
    }
    
    // Row 0: no shift
    state[0] = temp[0]; state[1] = temp[1]; state[2] = temp[2]; state[3] = temp[3];
    // Row 1: shift left by 1
    state[4] = temp[5]; state[5] = temp[6]; state[6] = temp[7]; state[7] = temp[4];
    // Row 2: shift left by 2
    state[8] = temp[10]; state[9] = temp[11]; state[10] = temp[8]; state[11] = temp[9];
    // Row 3: shift left by 3
    state[12] = temp[15]; state[13] = temp[12]; state[14] = temp[13]; state[15] = temp[14];
}

// Hamsi MixColumns transformation using Galois Field multiplication
__device__ void hamsi_mix_columns(uint32_t state[16]) {
    for (int col = 0; col < 4; col++) {
        uint8_t s0[4], s1[4], s2[4], s3[4];
        
        // Extract column bytes
        for (int i = 0; i < 4; i++) {
            s0[i] = (state[col + i * 4] >> 0) & 0xFF;
            s1[i] = (state[col + i * 4] >> 8) & 0xFF;
            s2[i] = (state[col + i * 4] >> 16) & 0xFF;
            s3[i] = (state[col + i * 4] >> 24) & 0xFF;
        }
        
        // Galois field multiplication: [2 3 1 1; 1 2 3 1; 1 1 2 3; 3 1 1 2] * column
        for (int i = 0; i < 4; i++) {
            uint8_t r0 = hamsi_mul2[s0[i]] ^ hamsi_mul3[s1[i]] ^ s2[i] ^ s3[i];
            uint8_t r1 = s0[i] ^ hamsi_mul2[s1[i]] ^ hamsi_mul3[s2[i]] ^ s3[i];
            uint8_t r2 = s0[i] ^ s1[i] ^ hamsi_mul2[s2[i]] ^ hamsi_mul3[s3[i]];
            uint8_t r3 = hamsi_mul3[s0[i]] ^ s1[i] ^ s2[i] ^ hamsi_mul2[s3[i]];
            
            state[col + i * 4] = ((uint32_t)r3 << 24) | ((uint32_t)r2 << 16) | 
                                ((uint32_t)r1 << 8) | (uint32_t)r0;
        }
    }
}

// Hamsi AddRoundConstant
__device__ void hamsi_add_round_constant(uint32_t state[16], int round) {
    // Simple round constant addition
    for (int i = 0; i < 16; i++) {
        state[i] ^= ((uint32_t)round << 28) | ((uint32_t)i << 24) | 0x00808000;
    }
}

// Hamsi round function
__device__ void hamsi_round(uint32_t state[16], int round) {
    hamsi_sub_bytes(state);
    hamsi_shift_rows(state);
    hamsi_mix_columns(state);
    hamsi_add_round_constant(state, round);
}

__device__ void cuda_hamsi512_impl(const uint8_t* input, uint32_t inputLen, uint8_t* output) {
    uint32_t state[16]; // 4x4 matrix for Hamsi
    
    // Initialize state with IV for Hamsi-512
    uint32_t iv[16] = {
        0x76657273, 0x696F6E20, 0x312E3000, 0x00000000,
        0x00000000, 0x00000000, 0x00000000, 0x00000000,
        0x00000000, 0x00000000, 0x00000000, 0x00000000,
        0x00000000, 0x00000000, 0x00000000, 0x00000200
    };
    
    for (int i = 0; i < 16; i++) {
        state[i] = iv[i];
    }
    
    // Process input in 64-byte blocks
    uint32_t pos = 0;
    while (pos + 64 <= inputLen) {
        uint32_t block[16];
        
        // Load block
        for (int i = 0; i < 16; i++) {
            block[i] = 0;
            for (int j = 0; j < 4; j++) {
                block[i] |= ((uint32_t)input[pos + i * 4 + j]) << (j * 8);
            }
        }
        
        // XOR block into state
        for (int i = 0; i < 16; i++) {
            state[i] ^= block[i];
        }
        
        // Apply rounds
        for (int round = 0; round < HAMSI_ROUNDS; round++) {
            hamsi_round(state, round);
        }
        
        pos += 64;
    }
    
    // Process remaining bytes with padding
    if (pos < inputLen) {
        uint8_t padded_block[64] = {0};
        uint32_t remaining = inputLen - pos;
        
        // Copy remaining bytes
        for (uint32_t i = 0; i < remaining; i++) {
            padded_block[i] = input[pos + i];
        }
        
        // Add padding bit
        padded_block[remaining] = 0x80;
        
        // Add length in bits at the end (little-endian)
        uint64_t bit_len = inputLen * 8;
        *((uint64_t*)(padded_block + 56)) = bit_len;
        
        // Process final block
        uint32_t block[16];
        for (int i = 0; i < 16; i++) {
            block[i] = 0;
            for (int j = 0; j < 4; j++) {
                block[i] |= ((uint32_t)padded_block[i * 4 + j]) << (j * 8);
            }
        }
        
        // XOR block into state
        for (int i = 0; i < 16; i++) {
            state[i] ^= block[i];
        }
        
        // Apply final rounds
        for (int round = 0; round < HAMSI_ROUNDS; round++) {
            hamsi_round(state, round);
        }
    }
    
    // Extract 512-bit output (64 bytes)
    for (int i = 0; i < 16; i++) {
        output[i * 4 + 0] = (state[i] >> 0) & 0xFF;
        output[i * 4 + 1] = (state[i] >> 8) & 0xFF;
        output[i * 4 + 2] = (state[i] >> 16) & 0xFF;
        output[i * 4 + 3] = (state[i] >> 24) & 0xFF;
    }
}
